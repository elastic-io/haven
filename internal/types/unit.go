package types

// 定义数据大小单位常量
const (
	// 基本单位 - 字节
	Byte = 1

	// 使用 iota 从 0 开始，每次增加 1
	// 左移 10 位相当于乘以 2^10 = 1024
	KB = 1 << (10 * iota) // 1 << (10 * 1) = 1024
	MB                    // 1 << (10 * 2) = 1,048,576
	GB                    // 1 << (10 * 3) = 1,073,741,824
	TB                    // 1 << (10 * 4) = 1,099,511,627,776
	PB                    // 1 << (10 * 5) = 1,125,899,906,842,624
)
